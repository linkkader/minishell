#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*temp;

	temp = lst;
	while (temp != NULL)
	{
		if (temp->next == NULL)
			break ;
		temp = temp->next;
	}
	return (temp);
}
