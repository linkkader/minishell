#include "../includes/minishell.h"

void	cd_builtin(char *path)
{
	printf("buitin cd %s\n", path);
	if (path == NULL)
		chdir("~");
	if (access(path, X_OK) == 0)
		chdir("/");
	else
		perror("cd");
}